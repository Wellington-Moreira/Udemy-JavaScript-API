//Make a program that displays a message telling if the typed number is a positive or negative number

const number = "0"

if (number > 0){
    console.log("Positive number!")
} else if (number < 0){
    console.log("Negative number!")
} else if (number === 0){
    console.log("The typed number is zero!")
} else {
    console.log("Not a number!")
}